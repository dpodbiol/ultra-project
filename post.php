					<h2>Aber deli bags Gold  at food Oscars! </h2>
					<img src="images/news1.jpg" alt="news1" />
					
					<p class="postedon">posted on 17 November 2011 by admin</p>

					<p>
					Aberystwyth based delicatessen Ultracomida have struck gold at this year’s True Taste Award, described as the food Oscars of the Welsh food and drinks industry, by bringing home the award for best Deli/Speciality shop.</p>
					<p>The shop and restaurant, specialising in Spanish delicacies and local Welsh produce,  fought off tough competition from across Wales to win the Gold for the best Deli/Speciality Store in the True Taste’s People and Places category.</p>
					<p>Ultracomida Director, Shumana Palit said: "We are so delighted to win the award because it reflects the passion with which we approach food and drink from Wales as well as Spain. Everyone involved in Ultracomida has devoted a great deal over the past year to maintaining high standards.  Our overall aim is to infuse our customers with our passion for food and drink and it is thanks to their continued support that we continue to be a success."</p>

					<p>Ultracomida has branches in Aberystwyth and Narberth, Pembrokeshire, and a new online shop which sell an array of Spanish wines, cheeses, rice and coffees. Many of these products cannot be found anywhere else in the UK and are showcased in the shops’ own on-site restaurants.
					Deputy Minister for Agriculture, Food and Fisheries, Alun Davies,  said: "These ‘People and Places’ categories recognise the valuable contribution made to the Welsh food, drink, hospitality and retail sectors by individuals and businesses. The efforts of those who cook, sell, and promote quality Welsh food and drink - and the environment in which it is produced - are an important part of the True Taste brand’s ethos."</p>
					<p>The Wales True Taste Food and Drink Awards were launched ten years ago to raise the profile of Welsh food and drink throughout Wales and beyond. This year there were over a 1000 entries from 366 companies across the16 categories.</p>

					<p>This is not the first award Ultracomida, having been recognised as the Observer Food Magazine best independent retailer in Wales, Ceredigion Business of the year, as well as True Taste Best Retailer in 2006 and 2008.

					</p>

